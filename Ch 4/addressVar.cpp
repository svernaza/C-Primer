#include <iostream>

using namespace std;

int main()
{
	//variable declarations
	int coffee = 10;
	double cups = 4.0;

	cout << "Coffee value: " << coffee << endl;
	cout << "Coffee Address: " << &coffee << endl;

	cout << "cups value: " << cups << endl;
	cout << "cups address: " << &cups << endl;

	return 0;
}
